total = 100

total += 10 #Le sumamos 10 al valor 100 de la variable total (total = total + 10)
total -= 10 # (total = total - 10)
total *= 10 #(total = total * 10)
total /= 2 #(total = total / 2)
total //= 3 #(total = total / 3 --> redondeado al número más bajo)
total **= 2 #(total = total**2)
total %= 3 #(total = total%3 --> cálculo del resto de la división)

print(total)

